#주사위 게임
#1부터 6까지 적힌 주사위 두개를 던져 나오는 값을 a,b라고 할때
#a,b 모두 홀수이면 a의 제곱 +b의 제곱 출력
#a,b모두 짝수이면 절대값(a-b)출력 (|a-b|) abs()
#a,b 둘 중의 하나만 홀 수이면 2*(a+b)를 출력하여라

a = int(input('1~6까지의 수 중 하나를 입력해주세요 >>>'))
b = int(input('1~6까지의 수 중 하나를 입력해주세요 >>>'))

if a%2 == 1 and b%2 == 1:
    print(a**2+b)

elif a%2 == 0 and b%2 == 0:
    print(abs(a-b))

else:
    print(2*(a+b))