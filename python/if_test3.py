#중국집 메뉴선택 조건

money = int(input('가지고 있는 돈을 입력하세요 >>>'))   #돈을 입력받는 함수

if money >= 20000:
    print('탕수육을 드실 수 있습니다')  #20000원 이상을 가지고 있다고 입력받으면 탕수육을 먹을 수 있다고 출력함

elif money >= 10000:
    print('쟁반짜장을 드실 수 있습니다') #10000원 이상을 가지고 있다고 입력받으면 쟁반짜장을 먹을 수 있다고 출력함

elif money == 6000:
    print('해물짬뽕을 드실 수 있습니다') #6000원을 가지고 있다고 입력받으면 해물짬뽕을 먹을 수 있다고 출력함

elif money >= 4000:
    print('짜장면을 드실 수 있습니다') #4000원 이상을 가지고 있다고 입력받으면 짜장면을 먹을 수 있다고 출력함

else:
    print('단무지를 드실 수 있습니다') #4000원 미만을 가지고 있으면 단무지를 출력함



#정수 리스트 num_list와 찾으려는 정수 n이 주어질때
